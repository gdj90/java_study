package chap03;
/*
 * 이항연산자
 *   산술연산자 : +(더하기),-(빼기),*(곱하기),/(나누기),%(나머지)
 */
public class OpEx03 {
	public static void main(String[] args) {
		System.out.println("10/8="+(10/8));
		System.out.println("-10/8="+(-10/8));
		System.out.println("10/-8="+(10/-8));
		System.out.println("-10/-8="+(-10/-8));
		System.out.println("10%8="+(10%8));
		System.out.println("-10%8="+(-10%8));
		System.out.println("10%-8="+(10%-8));
		System.out.println("-10%-8="+(-10%-8));

	}
}
